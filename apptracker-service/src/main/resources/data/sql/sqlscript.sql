  `DEMOGRAPH_UPD_TM` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `CURRENT_STATUS` bigint(20) NOT NULL,
  `CURRENT_STATUS_UPD_TM` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
