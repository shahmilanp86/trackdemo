  `LAST_UPD_TM` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `LAST_UPD_BY` varchar(20) DEFAULT NULL,
  `URL_STRING` varchar(30) DEFAULT NULL,
  `URL_CREATED_TM` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
